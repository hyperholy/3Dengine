int setPixel(int X, int Y, Uint32 Colour) {
    if (X <= 0 || X > WINDOW_WIDTH - 1) return -1;
    if (Y <= 0 || Y > WINDOW_HEIGHT - 1) return -1;
    gFrameBuffer[X + (Y * WINDOW_WIDTH)] = Colour;
    return 1;
}

Tri2d_t projectTri(Tri3d_t Tri){
    Tri2d_t PTri; 
    PTri.v0.x = (Tri.v0.x * FOCAL_LENGTH / (FOCAL_LENGTH + Tri.v0.z)) + (WINDOW_WIDTH / 2);
    PTri.v0.y = -(Tri.v0.y * FOCAL_LENGTH / (FOCAL_LENGTH + Tri.v0.z)) + (WINDOW_HEIGHT / 2);

    PTri.v1.x = (Tri.v1.x * FOCAL_LENGTH / (FOCAL_LENGTH + Tri.v1.z)) + (WINDOW_WIDTH / 2);
    PTri.v1.y = -(Tri.v1.y * FOCAL_LENGTH / (FOCAL_LENGTH + Tri.v1.z)) + (WINDOW_HEIGHT / 2);

    PTri.v2.x = (Tri.v2.x * FOCAL_LENGTH / (FOCAL_LENGTH + Tri.v2.z)) + (WINDOW_WIDTH / 2);
    PTri.v2.y = -(Tri.v2.y * FOCAL_LENGTH / (FOCAL_LENGTH + Tri.v2.z)) + (WINDOW_HEIGHT / 2);

    return PTri;
}

void interpolatei(std::vector<int> &values, float i0, float d0, float i1, float d1){//pass in a vector 
    if (i0 == i1) {
        values.clear();
        values.push_back(d0);
    }
    float a = (d1 - d0) / (i1 - i0);
    float d = d0;
    int i;
    for (i = i0; i <= i1; i++) {
        values.push_back(d);
        d += a;
    }
}

void interpolatef(std::vector<float> &values, float i0, float d0, float i1, float d1) {//pass in a vector 
    if (i0 == i1) {
        values.clear();
        values.push_back(d0);
    }
    float a = (d1 - d0) / (i1 - i0);
    float d = d0;
    int i;
    for (i = i0; i <= i1; i++) {
            values.push_back(d);
        d += a;
    }
}

Uint32 multiplyColour(Uint32 colour1, float n) {
    Uint8 r1, g1, b1, a1;
    SDL_GetRGBA(colour1, PIXELFORMAT, NULL, &r1, &g1, &b1, &a1);
    Uint8 r = (r1 * n);
    Uint8 g = (g1 * n);
    Uint8 b = (b1 * n);
    Uint8 a = (a1 * n);
    return(SDL_MapRGBA(PIXELFORMAT, NULL, r, g, b, a));
}

void translateMesh(struct std::vector<Tri3d_t> Mesh, Vec3d_t Transform, int size) {
    int i;
    for (i = 0; i < size; i++) {
        Mesh[i].v0.x += Transform.x;
        Mesh[i].v0.y += Transform.y;
        Mesh[i].v0.z += Transform.z;

        Mesh[i].v1.x += Transform.x;
        Mesh[i].v1.y += Transform.y;
        Mesh[i].v1.z += Transform.z;

        Mesh[i].v2.x += Transform.x;
        Mesh[i].v2.y += Transform.y;
        Mesh[i].v2.z += Transform.z;
    }
}

void rotateMesh(struct std::vector<Tri3d_t> Mesh, Vec3d_t Rotation, int size) {
    int i, j, k, l;
    Tri3d_t Rtri;
    translateMesh(Mesh, { 0,0,FOCAL_LENGTH }, size);
    float sinRZ = sin(Rotation.x);//alpha
    float cosRZ = cos(Rotation.x);
    float sinRY = sin(Rotation.y);//beta
    float cosRY = cos(Rotation.y);
    float sinRX = sin(Rotation.z);//gamma
    float cosRX = cos(Rotation.z);
    float multMatrix[3][3] = { {cosRZ * cosRY, cosRZ * sinRY * sinRX - sinRZ * cosRX                , cosRZ * sinRY * cosRX + sinRZ * sinRX}
                              ,{sinRZ * cosRY, sinRZ * sinRY * sinRX + cosRZ * cosRX        , sinRZ * sinRZ * sinRY * cosRX - cosRZ * sinRX}
                              ,{-sinRY       , cosRY * sinRX                                        , cosRY * cosRX} };

    for (i = 0; i < size; i++) {
        std::memcpy(&Rtri, &Mesh[i], sizeof(Tri3d_t));
        for (l = 0; l < 3; l++) {//iterate through triangle vertice pairs
            for (j = 0; j < 3; j++) {//dot product loop
                float sum = 0;
                for (k = 0; k < 3; k++) {//very safe memory accessing inbound
                    //so its accessing the pointer to the start of Mesh[] and adjusted the pointer offset by + ((...)) and reading the resulting address as a pointer to a float :)
                    sum += *(float*)((unsigned char*)&Mesh[i] + ((l * 12) + (k * 4))) * multMatrix[j][k];
                }//some more too
                //same thing here, fun fact, l is the y value of the matrix and k is the x of the matrix, i think
                *(float*)((unsigned char*)&Rtri + ((l * 12) + (j * 4))) = sum;
            }
        }
        std::memcpy(&Mesh[i], &Rtri, sizeof(Tri3d_t));
    }
    translateMesh(Mesh, { 0,0,-FOCAL_LENGTH }, size);
}

